    /**
     * Get the glyph bitmap for a character.
     * Returns 16 bytes, each byte representing one row (MSB = leftmost pixel).
     * Returns null for unsupported characters.
     */
    public static byte[] getGlyph(char c) {
        int idx = c - 32;
        if (idx < 0 || idx >= GLYPHS.length) return null;
        return GLYPHS[idx];
    }

    /**
     * Check if a specific pixel in a glyph is set.
     * @param glyph 16-byte glyph data
     * @param px pixel x (0-7, 0=left)
     * @param py pixel y (0-15, 0=top)
     */
    public static boolean isPixelSet(byte[] glyph, int px, int py) {
        return (glyph[py] & (0x80 >> px)) != 0;
    }

